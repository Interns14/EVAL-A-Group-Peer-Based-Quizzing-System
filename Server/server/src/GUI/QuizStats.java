    public QuizStats(HashMap<String,Integer> hm) {
        initComponents();
        
        Set<String> s = hm.keySet();
        
        String text = "";
        
        for( String key : s )
        {
            int a = hm.get(key);
            text = text + key+" : "+a+"  \n";
        }
        
        setQuizStat(text);
        
        wait = true;
    }

