    public void setFields(String question, String option1, String option2 , String option3, String option4, String answer)
    {
        jRadioButton1.setText(option1);
        jRadioButton2.setText(option2);
        jRadioButton3.setText(option3);
        jRadioButton4.setText(option4);
        
        if( option1.equals(answer) )
        {
            jRadioButton1.setSelected(true);
        }
        else if( option2.equals(answer) )
        {
            jRadioButton2.setSelected(true);
        }
        else if( option3.equals(answer) )
        {
            jRadioButton3.setSelected(true);
        }
        else if( option4.equals(answer) )
        {
            jRadioButton4.setSelected(true);
        }
        jLabel1.setText(question);
    }

