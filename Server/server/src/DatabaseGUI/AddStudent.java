    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
       
        
        String roll=new String(rollNumberField.getText());
        String name=new String(nameField.getText());
        String standard=new String(standardField.getSelectedItem().toString());
            
            // concatenating standard and roll number to generate a unique roll number
        String rollFinal=new String(standard.concat("_"));
        rollFinal=rollFinal.concat(roll);
        System.out.println(rollFinal);
            
            
            
            // to generate the password automatically, the First name and roll number isconcatenated
        String[] sub=name.split(" ",2);
        String word=sub[0];
        System.out.println(word);
        String password=new String(word+""+roll);
        System.out.println(password);
        //nameField.setText(String.valueOf(password));
        if(roll.equals("") || name.equals("") || standard.equals(""))
        {
            JOptionPane.showMessageDialog(this,Essentials.messages.getString("pleaseEnterAllFields"));
            clearButtonActionPerformed(evt);
         
            if(Essentials.objAddStudent==null)
            {
                Essentials.objAddStudent=new AddStudent();
            }
            Essentials.objAddStudent.setVisible(true);
        }
        else
        {
            try
            {
                Statement start=(Statement)Essentials.databaseConnection.createStatement();
                String query=new String("INSERT INTO student_info (roll_number,name,password,std,questions_attempted,correct_answers) VALUES ('"+rollFinal+"','"+name+"','"+password+"','"+standard+"','0','0')");
                int res=start.executeUpdate(query);
                if(res==0)
                {
                    System.out.println(Essentials.messages.getString("insertionFailed"));
                   // JOptionPane.showMessageDialog(this,Util.messages.getString("insertionSuccess")+Util.messages.getString("studentID"+rollFinal));
                }
                else
                {
                    JOptionPane.showMessageDialog(this,Essentials.messages.getString("insertionSuccess"));
                    if(Essentials.objConfigureStudent==null)
                    {
                        Essentials.objConfigureStudent=new ConfigureStudents();
                    }
                    Essentials.objConfigureStudent.setVisible(false);
                    Essentials.objAddStudent.setVisible(true);
                }
            }
            catch(MySQLIntegrityConstraintViolationException e)
            {
                JOptionPane.showMessageDialog(this,Essentials.messages.getString("duplicateEntry"));
                clearButtonActionPerformed(evt);

            }
            catch(NullPointerException e)
            {
                System.out.println("Null pointer");
                JOptionPane.showMessageDialog(this,Essentials.messages.getString("insertionFailed"));
                clearButtonActionPerformed(evt);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,Essentials.messages.getString("insertionFailed"));
                clearButtonActionPerformed(evt);
            }
        }
    }//GEN-LAST:event_submitButtonActionPerformed

