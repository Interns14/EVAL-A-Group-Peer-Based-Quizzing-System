    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        
                                    // checking the username and password
        String u=teacherIDField.getText();
        String p=passwordField.getText();
        String standard=new String(standardField.getSelectedItem().toString());
       
        
        // username and password should be filled
        try
        {
            String id=new String(teacherIDField.getText());
            String pass=new String(passwordField.getText());
                
            Statement start=(Statement)Essentials.databaseConnection.createStatement();
            String query=new String("SELECT * FROM teacher_login,teacher_detail WHERE teacher_login.teacher_id like '"+id+"' AND teacher_login.password like '"+pass+"' AND teacher_detail.std LIKE '"+standard+"'");
                
                
            ResultSet res=start.executeQuery(query);
                
                // If the teacher logs in, he/she gets her possible options
            if(res.next())
            {
                 if( Essentials.objTeacherOption==null)
                 {
                     Essentials.objTeacherOption=new TeacherOptions();
                 }
                 Essentials.objTeacherOption.setVisible(true);
                 Essentials.objTeacherLogin.setVisible(false);
            }
                
                // if the credentials do not match, then the teacher is asked to enter the details again
            else
            {
                JOptionPane.showMessageDialog(this,Essentials.messages.getString("loginFail"));
                clearButtonActionPerformed(evt);
            } 
                // JOptionPane.showMessageDialog(this,"Hello");
        } 
        catch (SQLException e) 
        {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
        catch (NullPointerException e)
        {
             JOptionPane.showMessageDialog(this,Essentials.messages.getString("loginFail"));
             clearButtonActionPerformed(evt);
        }
        
       
        
    }//GEN-LAST:event_submitButtonActionPerformed

