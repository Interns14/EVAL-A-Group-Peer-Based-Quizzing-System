    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        
         String tID=new String(teacherIDField.getText());
         String sub=new String(subjectField.getSelectedItem().toString());
         String stan=new String(standardField.getSelectedItem().toString()); 
         
         if(sub.equals("") ||  stan.equals(""))
         {
             JOptionPane.showMessageDialog(this,Essentials.messages.getString("insertionFailed"));
             System.out.println("Bad");
             //JOptionPane.showMessageDialog(this,"Update failed... Enter all the parameters!!");
                              //   JOptionPane.showMessageDialog(this,Essentials.messages.getString("insertionFailed"));

             clearButtonActionPerformed(evt);
         }
         else
         {        
            String query=new String("INSERT INTO teacher_detail VALUES ('"+tID+"','"+sub+"','"+stan+"')");
            try 
            {
                java.sql.Statement start=(java.sql.Statement)Essentials.databaseConnection.createStatement();
                int res=start.executeUpdate(query);

                if(res==0)
                {
                    JOptionPane.showMessageDialog(this,Essentials.messages.getString("insertionFailed"));
                    clearButtonActionPerformed(evt);
                }
                else
                {
                    JOptionPane.showMessageDialog(this,Essentials.messages.getString("insertionSuccess"));            
                    clearButtonActionPerformed(evt);

                }

            }
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(this,Essentials.messages.getString("insertionFailed"));            
               // System.out.println("Not done!!");
                clearButtonActionPerformed(evt);
            }
            catch(NullPointerException exx)
            {
                JOptionPane.showMessageDialog(this,Essentials.messages.getString("insertionFailed"));            
                clearButtonActionPerformed(evt);
            }
        }
      
            
    }//GEN-LAST:event_submitButtonActionPerformed

